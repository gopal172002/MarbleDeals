var myIndex1 = 0;

function carousel1() {
  var i;
  var x = document.getElementsByClassName("alb1");
  for (i = 0; i < x.length; i++) {
    x[i].style.display = "none";  
  }
  if (myIndex1 >= x.length) {
    myIndex1 = 0;
  }
  x[myIndex1].style.display = "block";  
  myIndex1++;
  setTimeout(carousel1, 2000);
}
